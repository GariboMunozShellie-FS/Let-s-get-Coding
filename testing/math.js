// Add Function
const add = (lNum, rNum) =>  lNum + rNum

// Subtract Function
const subtract = (lNum, rNum) =>  lNum - rNum

// Multiply Function
const multiply = (lNum, rNum) =>  lNum * rNum

// Divide Function
const divide = (lNum, rNum) =>  lNum / rNum

// Square Root Function
const sqrt = (num) =>  Math.sqrt(num)


module.exports = {
    add,
    subtract,
    multiply,
    divide,
    sqrt,
}